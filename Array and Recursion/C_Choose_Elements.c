#include<stdio.h>
int main()
{
    int n,k;
    scanf("%d %d",&n,&k);
    int a[n];
    for(int i=0; i<n; i++)
    {
        scanf("%d",&a[i]);
    }
    for(int i=0;i<n;i++)
    {
        for(int j=i+1; j<n; j++)
        {
            if(a[j] > a[i])
            {
                int tmp;
                tmp = a[i];
                a[i] = a[j];
                a[j] = tmp;
            }
        }
    }
    long long int sum = 0;
    for(int i=0; i<k; i++)
    {
        if(a[i] < 0)
        {
            break;
        }
        //  printf("%d ",a[i]);
        sum+=a[i];
    }
    // printf("\n");
     printf("%lld",sum);
    return 0;
}